
-- plan_site_conditions: soil/terrain data per obra
CREATE TABLE public.plan_site_conditions (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  obra_id uuid REFERENCES public.obras(id) ON DELETE CASCADE NOT NULL,
  user_id uuid NOT NULL,
  tipo_solo text NOT NULL DEFAULT 'misto',
  capacidade_portante_kpa numeric DEFAULT 150,
  nivel_freatico_m numeric DEFAULT 3.0,
  zona_sismica text DEFAULT 'B',
  topografia text DEFAULT 'plano',
  area_implantacao_m2 numeric DEFAULT 0,
  numero_pisos integer DEFAULT 1,
  observacoes text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE(obra_id)
);

ALTER TABLE public.plan_site_conditions ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Org members can view site conditions"
  ON public.plan_site_conditions FOR SELECT TO authenticated
  USING (user_id = ANY(public.get_org_member_ids()));

CREATE POLICY "Authenticated users can insert site conditions"
  ON public.plan_site_conditions FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Owners can update site conditions"
  ON public.plan_site_conditions FOR UPDATE TO authenticated
  USING (user_id = ANY(public.get_org_member_ids()));

CREATE POLICY "Owners can delete site conditions"
  ON public.plan_site_conditions FOR DELETE TO authenticated
  USING (user_id = ANY(public.get_org_member_ids()));

-- plan_infra_scenarios: foundation scenarios generated by Axia
CREATE TABLE public.plan_infra_scenarios (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  site_condition_id uuid REFERENCES public.plan_site_conditions(id) ON DELETE CASCADE NOT NULL,
  user_id uuid NOT NULL,
  nome text NOT NULL,
  tipo_fundacao text NOT NULL DEFAULT 'sapatas_isoladas',
  descricao text,
  parametros jsonb DEFAULT '{}'::jsonb,
  custo_estimado numeric DEFAULT 0,
  selecionado boolean DEFAULT false,
