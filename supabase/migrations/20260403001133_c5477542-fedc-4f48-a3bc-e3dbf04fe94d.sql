-- plan_infra_items: parametric items within a selected scenario
CREATE TABLE public.plan_infra_items (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  scenario_id uuid REFERENCES public.plan_infra_scenarios(id) ON DELETE CASCADE NOT NULL,
  artigo_base_id uuid,
  descricao text NOT NULL,
  unidade text NOT NULL DEFAULT 'm2',
  quantidade numeric NOT NULL DEFAULT 0,
  preco_unitario numeric NOT NULL DEFAULT 0,
  valor_total numeric NOT NULL DEFAULT 0,
  formula_origem text,
  created_at timestamptz NOT NULL DEFAULT now()
);

ALTER TABLE public.plan_infra_items ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Org members can view infra items"
  ON public.plan_infra_items FOR SELECT TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.plan_infra_scenarios s
      WHERE s.id = plan_infra_items.scenario_id
        AND s.user_id = ANY(public.get_org_member_ids())
    )
  );

CREATE POLICY "Authenticated users can insert infra items"
  ON public.plan_infra_items FOR INSERT TO authenticated
  WITH CHECK (
    EXISTS (
      SELECT 1 FROM public.plan_infra_scenarios s
      WHERE s.id = plan_infra_items.scenario_id
        AND s.user_id = ANY(public.get_org_member_ids())
    )
  );

CREATE POLICY "Owners can update infra items"
  ON public.plan_infra_items FOR UPDATE TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.plan_infra_scenarios s
      WHERE s.id = plan_infra_items.scenario_id
        AND s.user_id = ANY(public.get_org_member_ids())
    )
  );

CREATE POLICY "Owners can delete infra items"
  ON public.plan_infra_items FOR DELETE TO authenticated
  USING (
    EXISTS (
      SELECT 1 FROM public.plan_infra_scenarios s
      WHERE s.id = plan_infra_items.scenario_id
        AND s.user_id = ANY(public.get_org_member_ids())
    )
  );

