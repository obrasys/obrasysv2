-- =====================================================
-- BUDGET CATALOG TEMPLATES (Chapter + Article)
-- =====================================================

CREATE TABLE IF NOT EXISTS public.budget_chapter_templates (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  code text NOT NULL,
  name text NOT NULL,
  description text,
  sort_order integer NOT NULL DEFAULT 0,
  is_default boolean NOT NULL DEFAULT true,
  company_id uuid,
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX IF NOT EXISTS budget_chapter_templates_global_code_key
  ON public.budget_chapter_templates (code) WHERE company_id IS NULL;
CREATE UNIQUE INDEX IF NOT EXISTS budget_chapter_templates_company_code_key
  ON public.budget_chapter_templates (company_id, code) WHERE company_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS public.budget_article_templates (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  chapter_template_id uuid NOT NULL REFERENCES public.budget_chapter_templates(id) ON DELETE CASCADE,
  code text NOT NULL,
  name text NOT NULL,
  description text,
  suggested_unit text,
  category text,
  sort_order integer NOT NULL DEFAULT 0,
  is_default boolean NOT NULL DEFAULT true,
  company_id uuid,
  active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS budget_article_templates_chapter_idx
  ON public.budget_article_templates (chapter_template_id);
CREATE UNIQUE INDEX IF NOT EXISTS budget_article_templates_global_code_key
  ON public.budget_article_templates (code) WHERE company_id IS NULL;
CREATE UNIQUE INDEX IF NOT EXISTS budget_article_templates_company_code_key
  ON public.budget_article_templates (company_id, code) WHERE company_id IS NOT NULL;

DROP TRIGGER IF EXISTS trg_bct_updated_at ON public.budget_chapter_templates;
CREATE TRIGGER trg_bct_updated_at BEFORE UPDATE ON public.budget_chapter_templates
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
DROP TRIGGER IF EXISTS trg_bat_updated_at ON public.budget_article_templates;
CREATE TRIGGER trg_bat_updated_at BEFORE UPDATE ON public.budget_article_templates
  FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

ALTER TABLE public.budget_chapter_templates ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.budget_article_templates ENABLE ROW LEVEL SECURITY;

CREATE POLICY bct_select_globals_and_org ON public.budget_chapter_templates
  FOR SELECT TO authenticated
  USING ((company_id IS NULL AND active = true)
    OR company_id = public.get_user_org_id()
    OR public.is_super_admin());
CREATE POLICY bct_insert_org_admin ON public.budget_chapter_templates
  FOR INSERT TO authenticated
  WITH CHECK ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));
CREATE POLICY bct_update_scope ON public.budget_chapter_templates
  FOR UPDATE TO authenticated
  USING ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));
CREATE POLICY bct_delete_scope ON public.budget_chapter_templates
  FOR DELETE TO authenticated
  USING ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));

CREATE POLICY bat_select_globals_and_org ON public.budget_article_templates
  FOR SELECT TO authenticated
  USING ((company_id IS NULL AND active = true)
    OR company_id = public.get_user_org_id()
    OR public.is_super_admin());
CREATE POLICY bat_insert_org_admin ON public.budget_article_templates
  FOR INSERT TO authenticated
  WITH CHECK ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));
CREATE POLICY bat_update_scope ON public.budget_article_templates
  FOR UPDATE TO authenticated
  USING ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));
CREATE POLICY bat_delete_scope ON public.budget_article_templates
  FOR DELETE TO authenticated
  USING ((company_id = public.get_user_org_id() AND public.is_org_admin())
    OR (company_id IS NULL AND public.is_super_admin()));

ALTER TABLE public.artigos_orcamento
